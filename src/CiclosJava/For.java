package CiclosJava;

public class For {
    public static void main(String[] args) {
        System.out.println("ciclo for");
        // el for tiene inicializacion  , evaluacion ; actualizacion  solo se repite el paso 2 y 3

        for(int i = 1 ;i <=3;i++){
            System.out.println(i);
        }
    }
}
